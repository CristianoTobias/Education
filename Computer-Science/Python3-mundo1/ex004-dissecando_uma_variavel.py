text = input("Enter something: ")
print("It's primitive type is {}".format(type(text)))
print("Its numeric? {}".format(text.isnumeric()))
print("Its digit? {}".format(text.isdigit()))
print("Its alpha? {}".format(text.isalpha()))
print("It's only spaces? {}".format(text.isspace()))
print("It's uppercase? {}".format(text.isupper()))
print("It's lowercase? {}".format(text.islower()))
print("It's capitalized? {}".format(text.istitle()))



